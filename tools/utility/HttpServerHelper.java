package tools.utility;

import com.sun.net.httpserver.HttpHandler;
import com.sun.net.httpserver.HttpServer;
import org.apache.log4j.Logger;

import java.io.IOException;
import java.net.InetSocketAddress;

public class HttpServerHelper {

    private static Logger log = Logger.getLogger(HttpServerHelper.class);


    public HttpServerHelper() {


    }


    public void create(int port, HttpHandler httpHandler) {
        HttpServer server = null;
        try {
            server = HttpServer.create(new InetSocketAddress(port), 0);
        } catch (IOException e) {
            e.printStackTrace();
        }


        server.createContext("/packet", httpHandler);


        log.info("Server is starting at port:" + port);

        server.setExecutor(null);
        server.start();

    }


}
